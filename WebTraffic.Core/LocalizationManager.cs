    /// <summary>
    /// 全局多语言管理器（单例）。
    /// 默认语言：English (en)。
    /// 用法：LocalizationManager.Instance.T("key")
    /// </summary>
    public sealed class LocalizationManager
    {
        // ── 单例 ──────────────────────────────────────────────────
        private static readonly LocalizationManager _instance =
            new LocalizationManager();
        public static LocalizationManager Instance { get { return _instance; } }
        private LocalizationManager() { }

        // ── 事件 ──────────────────────────────────────────────────
        /// <summary>语言切换后触发，订阅者应在处理器中调用 ApplyLocalization()。</summary>
        public event EventHandler LanguageChanged;

        // ── 当前语言 ──────────────────────────────────────────────
        private string _currentCode = "en";
        public string CurrentCode { get { return _currentCode; } }

        /// <summary>切换语言并触发 LanguageChanged 事件。</summary>
        public void SetLanguage(string code)
        {
            if (_currentCode == code) return;
            if (!_translations.ContainsKey(code)) return;
            _currentCode = code;

            var handler = LanguageChanged;
            if (handler != null)
                handler(this, EventArgs.Empty);
        }

        /// <summary>从外部（AppSettingsRepository）加载持久化语言代码，在 Program.cs 启动时调用。</summary>
        public void LoadSavedLanguage(string savedCode)
        {
            if (!string.IsNullOrEmpty(savedCode) && _translations.ContainsKey(savedCode))
                _currentCode = savedCode;
            else
                _currentCode = "en";
        }

        // ── 翻译查询 ──────────────────────────────────────────────
        /// <summary>
        /// 返回当前语言的翻译文本。
        /// 若 key 缺失，依次回退：当前语言 → en → key 本身。
        /// </summary>
        public string T(string key)
        {
            Dictionary<string, string> dict;
            string value;

            if (_translations.TryGetValue(_currentCode, out dict) &&
                dict.TryGetValue(key, out value))
                return value;

            // 回退到英文
            if (_translations.TryGetValue("en", out dict) &&
                dict.TryGetValue(key, out value))
                return value;

            return key; // 最后返回 key 本身，方便排查缺失翻译
        }

